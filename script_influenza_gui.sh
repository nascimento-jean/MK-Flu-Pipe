else
    datasets_needed=()
    while IFS=$'\t' read -r sample tipo subtipo_ha subtipo_na hit_ha hit_na; do
        [ "$sample" = "sample" ] || [ "$sample" = "amostra" ] && continue
        key=$(get_nextclade_key "$tipo" "$subtipo_ha")
        if [ -n "$key" ]; then
            if ! printf '%s\n' "${datasets_needed[@]:-}" | grep -qx "$key"; then
                datasets_needed+=("$key")
            fi
        fi
    done < "$blast_summary"

    for key in "${datasets_needed[@]:-}"; do
        [ -z "$key" ] && continue
        _ensure_nextclade_dataset "$key"
    done

    while IFS=$'\t' read -r sample tipo subtipo_ha subtipo_na hit_ha hit_na; do
        [ "$sample" = "sample" ] || [ "$sample" = "amostra" ] && continue

        nc_out="$nextclade_per_sample_dir/${sample}_nextclade.tsv"

        if [ -f "$nc_out" ] && [ -s "$nc_out" ]; then
            log_skip "  Nextclade already done for $sample."
            continue
        fi

        nc_key=$(get_nextclade_key "$tipo" "$subtipo_ha")
        ha_file="$seg_base/single_segment4/${sample}_segment_4.fasta"

        echo -e "seqName\tclade\tshort-clade\tlegacy-clade\tlegacy-clade-vic\tlegacy-clade-yam\tlineage\tqc.overallStatus" > "$nc_out"

        if [ -z "$nc_key" ]; then
            log_warn "  $sample → Tipo '$tipo $subtipo_ha' no Nextclade dataset. BLAST only."
            echo -e "${sample}\tN/A\tN/A\tN/A\tN/A\tN/A\tN/A\tN/A" >> "$nc_out"
            continue
        fi

        if [ ! -s "$ha_file" ]; then
            log_warn "  $sample → HA file missing/empty. Skipping Nextclade."
            echo -e "${sample}\tN/A\tN/A\tN/A\tN/A\tN/A\tN/A\tN/A" >> "$nc_out"
            continue
        fi

        log_info "  Nextclade: $sample → dataset '$nc_key'"
        if ! $CONDA_RUN nextclade run \
            --input-dataset "$nextclade_datasets_dir/$nc_key" \
            --output-tsv "$nc_out" \
            "$ha_file" \
            >> "$run_log_file" 2>&1; then
            log_warn "  Nextclade failed for $sample."
            echo -e "${sample}\tERRO\tERRO\tERRO\tERRO\tERRO\tERRO\tERRO" >> "$nc_out"
        fi

    done < "$blast_summary"

