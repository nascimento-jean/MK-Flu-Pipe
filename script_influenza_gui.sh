parse_blast_hit() {
    local stitle="$1"
    local tipo subtipo

    if echo "$stitle" | grep -qiE "influenza A|type A|\(A/"; then
        tipo="A"
    elif echo "$stitle" | grep -qiE "influenza B|type B|\(B/"; then
        tipo="B"
    elif echo "$stitle" | grep -qiE "influenza C|\(C/"; then
        tipo="C"
    elif echo "$stitle" | grep -qiE "influenza D|\(D/"; then
        tipo="D"
    else
        tipo="Not determined"
    fi

    subtipo=$(echo "$stitle" | grep -oiE 'H[0-9]{1,2}N[0-9]{1,2}' | head -1 || true)

    if [ "$tipo" = "B" ] && [ -z "$subtipo" ]; then
        if echo "$stitle" | grep -qi "Victoria\|Vic"; then
            subtipo="Victoria"
        elif echo "$stitle" | grep -qi "Yamagata\|Yam"; then
            subtipo="Yamagata"
        else
            subtipo="nd"
        fi
    fi

    echo "${tipo}|${subtipo:-nd}"
}

get_nextclade_key() {
    local tipo="$1"
    local subtipo_ha="$2"
    local sub_upper="${subtipo_ha^^}"

    if [ "$tipo" = "A" ]; then
        case "$sub_upper" in
            H1N1*|H1) echo "H1N1" ;;
            H3N2*|H3) echo "H3N2" ;;
            H5*)       echo "H5"   ;;
            *)         echo "" ;;
        esac
    elif [ "$tipo" = "B" ]; then
        echo "B"
    else
        echo ""
    fi
}

parse_nextclade_clade() {
    local nc_file="$1"
    local tipo="$2"   # "A" or "B" — determines which columns to use

    awk -F'\t' -v tipo="$tipo" '
        NR == 1 {
            for (i = 1; i <= NF; i++) {
                col[$i] = i
            }
            next
        }
        NR == 2 {
            # Helper: get column value or empty string
            def_col["clade"]              = col["clade"]              ? $col["clade"]              : ""
            def_col["legacy-clade"]       = col["legacy-clade"]       ? $col["legacy-clade"]       : ""
            def_col["legacy-clade-vic"]   = col["legacy-clade-vic"]   ? $col["legacy-clade-vic"]   : ""
            def_col["legacy-clade-yam"]   = col["legacy-clade-yam"]   ? $col["legacy-clade-yam"]   : ""
            def_col["lineage"]            = col["lineage"]            ? $col["lineage"]            : ""
            def_col["short-clade"]        = col["short-clade"]        ? $col["short-clade"]        : ""

            clade      = def_col["clade"]
            legacy     = def_col["legacy-clade"]
            vic        = def_col["legacy-clade-vic"]
            yam        = def_col["legacy-clade-yam"]
            lineage    = def_col["lineage"]

            if (clade == "N/A" || clade == "ERRO" || clade == "") {
                print "\xe2\x80\x94"
                exit
            }

            if (tipo == "B") {
                # For Flu B: the generic dataset reports either Victoria or Yamagata.
                # One of legacy-clade-vic / legacy-clade-yam will be "unassigned";
                # the other carries the actual clade value.
                b_leg = ""
                if (vic != "" && vic != "unassigned" && vic != "N/A") {
                    b_leg = vic
                } else if (yam != "" && yam != "unassigned" && yam != "N/A") {
                    b_leg = yam
                }

                # Combine with lineage if available and different
                if (b_leg != "") {
                    if (lineage != "" && lineage != "N/A" && lineage != b_leg) {
                        print b_leg "/" lineage
                    } else {
                        print b_leg
                    }
                } else {
                    # Fallback: use clade alone
                    if (lineage != "" && lineage != "N/A") {
                        print clade "/" lineage
                    } else {
                        print clade
                    }
                }
                exit
            }

            # Flu A (and others): clade / legacy-clade
            if (legacy != "" && legacy != "N/A" && legacy != clade) {
                print clade "/" legacy
                exit
            }

            # Fallback: clade only
            print clade
        }
    ' "$nc_file" 2>/dev/null || echo "—"
}

