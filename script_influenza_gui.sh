    def _mut_class(gene, alt_aa, mutation):
        alt = str(alt_aa)
        if alt.startswith('+'): return 'insertion'
        if alt.startswith('-'): return 'deletion'
        if gene == 'UTR':       return 'utr'
        if str(mutation) == '.': return 'synonymous'
        return 'nonsynonymous'

    def _mut_label(gene, aa_pos, ref_aa, alt_aa, mutation, mclass):
        if mclass == 'insertion':
            return f"{gene}:{ref_aa}{aa_pos}ins{str(alt_aa).lstrip('+')}"
        if mclass == 'deletion':
            return f"{gene}:{ref_aa}{aa_pos}del{str(alt_aa).lstrip('-')}"
        if mclass == 'nonsynonymous':
            return f"{gene}:{mutation}"
        return None

