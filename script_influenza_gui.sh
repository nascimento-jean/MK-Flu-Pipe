        continue
    fi

    log_info "[$sample_current/$total_amostras] Processing: $file_name"

    sample_output_dir="$output_dir/$file_name"
    mem_before=$(free -m | awk '/Mem/ {printf "%d", $3}')
    mem_pct=$(free -m | awk '/Mem/ {printf "%.1f", $3/$2*100}')
    cores=$(nproc --all)

    {
        echo ""
        echo "  Mode: $SEQ_MODE | Memory before: ${mem_before} MB (${mem_pct}%) | Cores: $cores"
    } >> "$run_log_file"

    irma_ok=0

    case "$SEQ_MODE" in

        short_paired)
            if [ -z "$input_file_r2" ] || [ ! -f "$input_file_r2" ]; then
                log_warn "  R2 not found for '$file_name'. Skipping."
                samples_failed=$((samples_failed + 1))
                continue
            fi
            if IRMA "$irma_module" "$input_file_r1" "$input_file_r2" "$sample_output_dir" \
                    2>&1 | tee -a "$run_log_file"; then
                irma_ok=1
            fi
            ;;

        single|sra_single)
            if IRMA "$irma_module" "$input_file_r1" "$sample_output_dir" \
                    2>&1 | tee -a "$run_log_file"; then
                irma_ok=1
            fi
            ;;
    esac

    if [ "$irma_ok" -eq 1 ]; then
        amended_dir="$sample_output_dir/amended_consensus"
        if [ -d "$amended_dir" ] && compgen -G "$amended_dir/*.fa" > /dev/null; then
            cat "$amended_dir"/*.fa > "$output_dir/assembly_final/$file_name.fasta"
            log_info "  → Consensus: assembly_final/$file_name.fasta"
            mark_done "$checkpoint_irma"
            samples_ok=$((samples_ok + 1))
        else
            log_warn "  → amended_consensus empty/missing for '$file_name'."
            samples_failed=$((samples_failed + 1))
        fi
    else
        log_error "  → IRMA failed for '$file_name'."
        samples_failed=$((samples_failed + 1))
    fi

    mem_after=$(free -m | awk '/Mem/ {print $3}')
    mem_pct2=$(free -m | awk '/Mem/ {printf "%.1f", $3/$2*100}')
    echo "  Memory after: ${mem_after} MB (${mem_pct2}%)" >> "$run_log_file"
done < "$processed_list_file"

