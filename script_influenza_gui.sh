_ensure_nextclade_dataset() {
    local key="$1"
    local dataset="${NEXTCLADE_DATASETS[$key]}"
    local dest="$nextclade_datasets_dir/$key"
    local ts="$dest/.last_update"

    local days_old=999
    [ -f "$ts" ] && days_old=$(days_since_modified "$ts")

    if [ ! -d "$dest" ] || [ ! -f "$dest/pathogen.json" ]; then
        log_info "  Nextclade dataset '$key' not found. Downloading ($dataset)..."
        $CONDA_RUN nextclade dataset get \
            --name "$dataset" \
            --output-dir "$dest" \
            >> "$run_log_file" 2>&1
        date '+%Y-%m-%d' > "$ts"
        log_info "  Dataset '$key' ready."
    elif [ "$days_old" -gt "$NC_MAX_DAYS" ]; then
        log_info "  Nextclade dataset '$key' is ${days_old} day(s) old (limit: ${NC_MAX_DAYS}). Updating..."
        $CONDA_RUN nextclade dataset get \
            --name "$dataset" \
            --output-dir "$dest" \
            >> "$run_log_file" 2>&1
        date '+%Y-%m-%d' > "$ts"
        log_info "  Dataset '$key' updated."
    else
        log_info "  Dataset '$key' OK — updated ${days_old} day(s)."
    fi
}

if is_done "$checkpoint_nextclade"; then
