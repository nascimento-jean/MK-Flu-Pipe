_download_ncbi_db() {
    log_info "Downloading NCBI Influenza database..."
    local gz_file="$DB_DIR/influenza.fna.gz"

    if ! wget -q --show-progress -O "$gz_file" "$DB_URL"; then
        log_warn "Download failed. No connection?"
        rm -f "$gz_file"
        return 1
    fi

    log_info "Decompressing..."
    gunzip -f "$gz_file"

    log_info "Formatting BLAST database (makeblastdb)..."
    $CONDA_RUN makeblastdb \
        -in "$DB_FILE" \
        -dbtype nucl \
        -out "$DB_BLAST" \
        -title "NCBI_Influenza" \
        -parse_seqids \
        >> "$run_log_file" 2>&1

    date '+%Y-%m-%d' > "$DB_TIMESTAMP"
    log_info "Database ready at: $DB_DIR"
    return 0
}

if is_done "$checkpoint_db" && [ -f "$DB_FILE" ] && [ -f "${DB_BLAST}.nhr" ]; then
    log_skip "NCBI database already prepared this run (checkpoint). Skipping Step 7."
else
    if [ ! -f "$DB_FILE" ] || [ ! -f "${DB_BLAST}.nhr" ]; then
        log_info "Database not found. Performing initial download..."
        _download_ncbi_db || { log_error "Could not download NCBI Influenza database."; exit 1; }
    else
        days_old=0
        [ -f "$DB_TIMESTAMP" ] && days_old=$(days_since_modified "$DB_TIMESTAMP") || days_old=$((DB_MAX_DAYS + 1))

        if [ "$days_old" -gt "$DB_MAX_DAYS" ]; then
            log_warn "Database is ${days_old} days old (limit: ${DB_MAX_DAYS}). Attempting update..."
            if _download_ncbi_db; then
                log_info "Database updated successfully."
            else
                log_warn "Update failed. Continuing with existing database (${days_old} days old)."
            fi
        else
            log_info "NCBI database OK — updated ${days_old} day(s)."
        fi
    fi
    mark_done "$checkpoint_db"
fi

