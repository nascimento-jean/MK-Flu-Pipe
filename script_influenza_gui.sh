if is_done "$checkpoint_trim"; then
    log_skip "Trimming already done (checkpoint). Skipping Step 2."
else
    while IFS=$'\t' read -r file_name input_file_r1 input_file_r2; do

        # Arquivos intermediários
        trimmed_r1="$TRIMMED_DIR/${file_name}_trimmed_R1.fastq.gz"
        trimmed_r2="$TRIMMED_DIR/${file_name}_trimmed_R2.fastq.gz"
        depleted_r1="$DEPLETED_DIR/${file_name}_depleted_R1.fastq.gz"
        depleted_r2="$DEPLETED_DIR/${file_name}_depleted_R2.fastq.gz"

        log_info "  Processing pre-IRMA QC: $file_name"

        # ── Step 0.5 / 0.7: Trimming ──────────────────────────────────────
        case "$SEQ_MODE" in

            short_paired)
                if conda_has_tool "fastp"; then
                    run_fastp_paired "$file_name" \
                        "$input_file_r1" "$input_file_r2" \
                        "$trimmed_r1" "$trimmed_r2" \
                        "$FASTP_REPORT_DIR"
                    log_info "    fastp paired OK: $file_name"
                else
                    log_warn "    fastp not found. Using raw reads."
                    cp "$input_file_r1" "$trimmed_r1"
                    cp "$input_file_r2" "$trimmed_r2"
                fi
                ;;

            single|sra_single)
                if [ "$IS_LONGREAD" = "yes" ]; then
                    # ONT/PacBio → Filtlong
                    if conda_has_tool "filtlong"; then
                        run_filtlong "$file_name" \
                            "$input_file_r1" "$trimmed_r1" \
                            "$FASTP_REPORT_DIR"
                        log_info "    Filtlong OK: $file_name"
                    else
                        log_warn "    filtlong not found. Using raw reads."
                        cp "$input_file_r1" "$trimmed_r1"
                    fi
                else
                    # Single-end Illumina / Ion Torrent → fastp
                    if conda_has_tool "fastp"; then
                        run_fastp_single "$file_name" \
                            "$input_file_r1" "$trimmed_r1" \
                            "$FASTP_REPORT_DIR"
                        log_info "    fastp single OK: $file_name"
                    else
                        log_warn "    fastp not found. Using raw reads."
                        cp "$input_file_r1" "$trimmed_r1"
                    fi
                fi
                ;;
        esac

        # ── Step 0.8: Host depletion ───────────────────────────────────────
        if [ "$HOST_DEPLETION" = "yes" ]; then
            case "$SEQ_MODE" in
                short_paired)
                    if conda_has_tool "bowtie2" && [ -f "${HUMAN_INDEX}.1.bt2" -o -f "${HUMAN_INDEX}.1.bt2l" ]; then
                        run_host_depletion_bowtie2_paired "$file_name" \
                            "$trimmed_r1" "$trimmed_r2" \
                            "$depleted_r1" "$depleted_r2" \
                            "$DEPLETED_DIR"
                        # Use depleted reads as input; fallback to trimmed if empty
                        [ -s "$depleted_r1" ] || { cp "$trimmed_r1" "$depleted_r1"; cp "$trimmed_r2" "$depleted_r2"; }
                        log_info "    Bowtie2 depletion OK: $file_name"
                    else
                        log_warn "    bowtie2 or human index not found. Skipping depletion."
                        cp "$trimmed_r1" "$depleted_r1"
                        [ -f "$trimmed_r2" ] && cp "$trimmed_r2" "$depleted_r2"
                    fi
                    ;;
                single|sra_single)
                    if [ "$IS_LONGREAD" = "yes" ]; then
                        if conda_has_tool "minimap2" && conda_has_tool "samtools" && [ -f "$HUMAN_FA" ]; then
                            run_host_depletion_minimap2 "$file_name" \
                                "$trimmed_r1" "$depleted_r1" \
                                "$DEPLETED_DIR"
                            [ -s "$depleted_r1" ] || cp "$trimmed_r1" "$depleted_r1"
                            log_info "    minimap2 depletion OK: $file_name"
                        else
                            log_warn "    minimap2/samtools not found. Skipping depletion."
                            cp "$trimmed_r1" "$depleted_r1"
                        fi
                    else
                        if conda_has_tool "bowtie2" && [ -f "${HUMAN_INDEX}.1.bt2" -o -f "${HUMAN_INDEX}.1.bt2l" ]; then
                            run_host_depletion_bowtie2_single "$file_name" \
                                "$trimmed_r1" "$depleted_r1" \
                                "$DEPLETED_DIR"
                            [ -s "$depleted_r1" ] || cp "$trimmed_r1" "$depleted_r1"
                            log_info "    Bowtie2 single depletion OK: $file_name"
                        else
                            log_warn "    bowtie2 not found. Skipping depletion."
                            cp "$trimmed_r1" "$depleted_r1"
                        fi
                    fi
                    ;;
            esac
        else
            # Sem depleção: depleted = trimmed
            cp "$trimmed_r1" "$depleted_r1" 2>/dev/null || true
            [ -f "$trimmed_r2" ] && cp "$trimmed_r2" "$depleted_r2" 2>/dev/null || true
        fi

    done < "$samples_list_file"

    mark_done "$checkpoint_trim"
    log_info "Trimming and depletion complete."
fi

