# Post-assembly QC: analyzes coverage and %N from IRMA output files
run_assembly_qc() {
    local sample="$1"
    local sample_irma_dir="$2"
    local fasta_file="$3"

    local tables_dir="$sample_irma_dir/tables"
    local n_segs=0
    local total_cov=0
    local low_cov_segs=""
    local high_n_segs=""
    local qc_status="PASS"

    # Count segments in final FASTA
    if [ -f "$fasta_file" ] && [ -s "$fasta_file" ]; then
        n_segs=$(grep -c '^>' "$fasta_file" || echo 0)
    fi

    # Check coverage and %N per segment via FASTA
    if [ -f "$fasta_file" ] && [ -s "$fasta_file" ]; then
        while IFS= read -r line; do
            if [[ "$line" == ">"* ]]; then
                current_header="$line"
                current_seq=""
            else
                current_seq+="$line"
                # Calculate %N
                total_bases=${#current_seq}
                if [ "$total_bases" -gt 0 ]; then
                    n_count=$(echo "$current_seq" | tr -cd 'Nn' | wc -c)
                    n_pct=$(awk "BEGIN {printf \"%.1f\", $n_count/$total_bases*100}")
                    seg_name=$(echo "$current_header" | sed 's/>//' | awk '{print $1}')
                    if awk "BEGIN {exit !($n_pct > $MAX_N_PCT)}"; then
                        high_n_segs="${high_n_segs}${seg_name}(${n_pct}%N) "
                        qc_status="WARN"
                    fi
                fi
            fi
        done < "$fasta_file"
    fi

    # Check minimum coverage via IRMA tables (coverage.txt per segment)
    if [ -d "$tables_dir" ]; then
        for cov_file in "$tables_dir"/*-coverage.txt; do
            [ -f "$cov_file" ] || continue
            seg_name=$(basename "$cov_file" -coverage.txt)
            avg_cov=$(LC_ALL=C awk 'NR>1 {sum+=$3; n++} END {if(n>0) printf "%.0f", sum/n; else print 0}' "$cov_file" 2>/dev/null || echo 0)
            if [ "$avg_cov" -lt "$MIN_COVERAGE" ] 2>/dev/null; then
                low_cov_segs="${low_cov_segs}${seg_name}(${avg_cov}x) "
                qc_status="WARN"
            fi
        done
    fi

    # Failure if too few segments assembled
    if [ "$n_segs" -lt "$MIN_SEGMENTS" ]; then
        qc_status="FAIL"
    fi

    # Build QC details string
    local qc_detail="segs:${n_segs}/8"
    [ -n "$low_cov_segs" ] && qc_detail="${qc_detail}|low_cov:${low_cov_segs%% }"
    [ -n "$high_n_segs"  ] && qc_detail="${qc_detail}|high_N:${high_n_segs%% }"

    echo "${qc_status}|${qc_detail}"
}

