# FastQC on raw FASTQ files (one or two files)
run_fastqc() {
    local outdir="$1"
    shift
    local files=("$@")
    mkdir -p "$outdir"
    $CONDA_RUN fastqc \
        --outdir "$outdir" \
        --threads "$(nproc --all)" \
        --quiet \
        "${files[@]}" >> "$run_log_file" 2>&1 || true
}

# fastp for paired-end (Illumina)
run_fastp_paired() {
    local sample="$1"
    local r1="$2"
    local r2="$3"
    local out_r1="$4"
    local out_r2="$5"
    local report_dir="$6"

    mkdir -p "$report_dir"

    local adapter_arg=""
    if [ -n "$ADAPTER_FASTA" ] && [ -f "$ADAPTER_FASTA" ]; then
        adapter_arg="--adapter_fasta ${ADAPTER_FASTA}"
    else
        adapter_arg="--detect_adapter_for_pe"
    fi

    # Quality parameters
    local fastp_cmd=(
        $CONDA_RUN fastp
        --in1 "$r1" --in2 "$r2"
        --out1 "$out_r1" --out2 "$out_r2"
        --length_required "$MIN_LEN_ILLUMINA"
        --qualified_quality_phred "$MIN_QUAL"
        --cut_tail
        --cut_front
        --thread "$(nproc --all)"
        --json "$report_dir/${sample}_fastp.json"
        --html "$report_dir/${sample}_fastp.html"
        --report_title "fastp — ${sample}"
    )
    # Adds adapter argument separately (cannot be in array with expansion)
    if [ -n "$ADAPTER_FASTA" ] && [ -f "$ADAPTER_FASTA" ]; then
        "${fastp_cmd[@]}" --adapter_fasta "$ADAPTER_FASTA" >> "$run_log_file" 2>&1
    else
        "${fastp_cmd[@]}" --detect_adapter_for_pe >> "$run_log_file" 2>&1
    fi
}

# fastp for single-end Illumina / Ion Torrent (not ONT)
run_fastp_single() {
    local sample="$1"
    local r1="$2"
    local out_r1="$3"
    local report_dir="$4"

    mkdir -p "$report_dir"

    local fastp_cmd=(
        $CONDA_RUN fastp
        --in1 "$r1"
        --out1 "$out_r1"
        --length_required "$MIN_LEN_ILLUMINA"
        --qualified_quality_phred "$MIN_QUAL"
        --cut_tail
        --cut_front
        --thread "$(nproc --all)"
        --json "$report_dir/${sample}_fastp.json"
        --html "$report_dir/${sample}_fastp.html"
        --report_title "fastp — ${sample}"
    )
    if [ -n "$ADAPTER_FASTA" ] && [ -f "$ADAPTER_FASTA" ]; then
        "${fastp_cmd[@]}" --adapter_fasta "$ADAPTER_FASTA" >> "$run_log_file" 2>&1
    else
        "${fastp_cmd[@]}" >> "$run_log_file" 2>&1
    fi
}

# Filtlong for long reads (ONT / PacBio)
run_filtlong() {
    local sample="$1"
    local r1="$2"
    local out_r1="$3"
    local report_dir="$4"

    mkdir -p "$report_dir"

