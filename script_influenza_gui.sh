def badge(v):
    lv = v.strip().lower()
    if   lv == 'pass':             c = 'bg-success'
    elif lv == 'warn':             c = 'bg-warning text-dark'
    elif lv == 'fail':             c = 'bg-danger'
    elif lv == 'good':             c = 'bg-success'
    elif lv == 'mediocre':         c = 'bg-warning text-dark'
    elif lv == 'bad':              c = 'bg-danger'
    elif lv == 'ok':               c = 'bg-success'
    elif 'blast+nextclade' in lv:  c = 'bg-primary'
    elif lv == 'wt':               c = 'bg-success'
    elif lv == 'verify':           c = 'bg-warning text-dark'
    elif lv == 'mut_detected':     c = 'bg-danger'
    elif lv == 'variant':
        return '<span class="badge rounded-pill" style="background:#fd7e14;color:#fff">{}</span>'.format(hl.escape(v))
    elif lv == 'no_data':          c = 'bg-secondary'
    else:                          c = 'bg-secondary'
    return '<span class="badge {} rounded-pill">{}</span>'.format(c, hl.escape(v))

def cell(h, v):
    if str(v).strip() == "nd": v = "-"
    h_low = h.lower()
    if h_low == 'type':
        if v == 'A':
            return '<span class="badge rounded-pill" style="background:#66CDAA;color:#1a4a3a">A</span>'
        elif v == 'B':
            return '<span class="badge rounded-pill" style="background:#FFDEAD;color:#5a3e1b">B</span>'
    if any(k in h_low for k in ('qc_assembly','qc_nextclade','classification_source',
                                 'status_coinf','status')):
        return badge(v)
    # Nomenclatura de substituição — destaca mutações detectadas em vermelho
    if h_low == 'nomenclature' and v not in ('N/D', '?', ''):
        parts = v.replace('?','')
        if len(parts) >= 3 and parts[0] == parts[-1]:
            return '<span style="color:#198754;font-weight:600">{}</span>'.format(hl.escape(v))
        return '<span style="color:#dc3545;font-weight:700">{}</span>'.format(hl.escape(v))
    # Protein mutation column: . = synonymous/no change (muted), else colored by type
    if h_low == 'mutation':
        if v in ('.', '', '-'):
            return '<span style="color:#adb5bd;font-size:.78rem">syn</span>'
        # Stop codon
        if '*' in v:
            return '<span style="color:#dc3545;font-weight:700;background:#ffeaea;padding:1px 4px;border-radius:3px">{}</span>'.format(hl.escape(v))
        return '<span style="color:#6f42c1;font-weight:700">{}</span>'.format(hl.escape(v))
    # frequency column: color by value
    if h_low in ('frequency', 'freq', 'alt_freq'):
        try:
            fv = float(v.replace('%',''))
            if fv >= 80:   col = '#198754'
            elif fv >= 30: col = '#fd7e14'
            else:          col = '#dc3545'
            return '<span style="color:{};font-weight:600">{}</span>'.format(col, hl.escape(v))
        except Exception:
            pass
    return hl.escape(str(v))

