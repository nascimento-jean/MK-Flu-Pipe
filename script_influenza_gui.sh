# Reads a GFF3 file and returns the canonical segment number and name
# by inspecting the CDS gene= annotations — works for FluA and FluB.
# Usage: _seg_from_gff3 GFF3_FILE → "SEGNUM SEGNAME"  e.g. "6 NA"
# Gene → segment map:
#   PB2            → 1 PB2 | PB1, PB1-F2  → 2 PB1 | PA, PA-X → 3 PA
#   HA             → 4 HA  | NP           → 5 NP  | NA, NB   → 6 NA
#   M1, M2, BM2, MP→ 7 MP | NS1, NS2, NEP→ 8 NS
_seg_from_gff3() {
    local gff3="$1"
    [ -f "$gff3" ] && [ -s "$gff3" ] || { echo ""; return 0; }
    awk -F'\t' '
    BEGIN {
        SEG["PB2"]=1;   NAME[1]="PB2"
        SEG["PB1"]=2;   NAME[2]="PB1";  SEG["PB1-F2"]=2
        SEG["PA"]=3;    NAME[3]="PA";   SEG["PA-X"]=3
        SEG["HA"]=4;    NAME[4]="HA"
        SEG["NP"]=5;    NAME[5]="NP"
        SEG["NA"]=6;    NAME[6]="NA";   SEG["NB"]=6
        SEG["M1"]=7;    NAME[7]="MP";   SEG["M2"]=7; SEG["BM2"]=7; SEG["MP"]=7
        SEG["NS1"]=8;   NAME[8]="NS";   SEG["NS2"]=8; SEG["NEP"]=8
        found_seg=""; found_name=""
    }
    $3 == "CDS" {
        n = split($9, attrs, ";")
        for (i=1; i<=n; i++) {
            if (attrs[i] ~ /^gene=/) {
                gene = substr(attrs[i], 6)
                gsub(/[[:space:]]/, "", gene)
                g = toupper(gene)
                if (g in SEG && found_seg == "") {
                    found_seg  = SEG[g]
                    found_name = NAME[found_seg]
                }
            }
        }
    }
    END { if (found_seg != "") print found_seg " " found_name }
    ' "$gff3"
}

# Downloads a RefSeq FASTA + GFF3 and caches in REFSEQ_CACHE_DIR.
# Usage: _ensure_refseq ACC → path to FASTA, path to GFF3 (one per line)
REFSEQ_CACHE_DIR="$DB_DIR/refseq_segments"

# One-time migration: remove cached files from old non-NC_* accessions.
# CY*/JN*/AY*/KF* records do not have GFF3 on NCBI.
# NC_* are the correct curated RefSeqs with guaranteed GFF3.
_migrate_refseq_cache() {
    local cache="$REFSEQ_CACHE_DIR"
    [ -d "$cache" ] || return 0
    local removed=0
    for f in "$cache"/CY*.fa  "$cache"/CY*.gff3 "$cache"/CY*.fa.*               "$cache"/JN*.fa  "$cache"/JN*.gff3 "$cache"/JN*.fa.*               "$cache"/AY*.fa  "$cache"/AY*.gff3 "$cache"/AY*.fa.*               "$cache"/KF*.fa  "$cache"/KF*.gff3 "$cache"/KF*.fa.*; do
        [ -f "$f" ] || continue
        rm -f "$f"
        removed=$((removed + 1))
    done
    [ "$removed" -gt 0 ] &&         log_info "  RefSeq cache: removed $removed old non-NC_* file(s) — will re-download as NC_* RefSeqs."
    return 0
}

_ensure_refseq() {
    local acc="$1"
    mkdir -p "$REFSEQ_CACHE_DIR"
    local fa_file="$REFSEQ_CACHE_DIR/${acc}.fa"
    local gff_file="$REFSEQ_CACHE_DIR/${acc}.gff3"

    # Download FASTA if missing
    if [ ! -s "$fa_file" ]; then
        log_info "    Downloading RefSeq FASTA: $acc" >&2
        local url_fa="https://eutils.ncbi.nlm.nih.gov/entrez/eutils/efetch.fcgi?db=nuccore&id=${acc}&rettype=fasta&retmode=text"
        if conda_has_tool "efetch"; then
            $CONDA_RUN efetch -db nuccore -id "$acc" -format fasta \
                > "$fa_file" 2>> "$run_log_file" || rm -f "$fa_file"
        fi
        if [ ! -s "$fa_file" ]; then
            wget -q --timeout=30 -O "$fa_file" "$url_fa" 2>> "$run_log_file" || rm -f "$fa_file"
        fi
        if [ ! -s "$fa_file" ]; then
            log_warn "    Could not download FASTA for $acc. Skipping segment." >&2
            echo ""; echo ""
            return 1
        fi
    fi

    # Download GFF3 if missing
    if [ ! -s "$gff_file" ]; then
        log_info "    Downloading RefSeq GFF3: $acc" >&2
        local url_gff="https://eutils.ncbi.nlm.nih.gov/entrez/eutils/efetch.fcgi?db=nuccore&id=${acc}&rettype=gff3&retmode=text"
        if conda_has_tool "efetch"; then
            $CONDA_RUN efetch -db nuccore -id "$acc" -format gff3 \
                > "$gff_file" 2>> "$run_log_file" || rm -f "$gff_file"
        fi
        if [ ! -s "$gff_file" ]; then
            wget -q --timeout=30 -O "$gff_file" "$url_gff" 2>> "$run_log_file" || rm -f "$gff_file"
        fi
        # Validate: GFF3 must contain at least one CDS line
        if [ ! -s "$gff_file" ]; then
            log_warn "    GFF3 download failed for $acc — protein translation will be skipped." >&2
            log_warn "    Tip: NC_* accessions always have GFF3. Check internet access." >&2
            echo "$fa_file"; echo ""
            return 0
        fi
        if ! grep -q $'\tCDS\t' "$gff_file" 2>/dev/null; then
            log_warn "    GFF3 for $acc has no CDS feature — file may be corrupt. Deleting for re-download." >&2
            rm -f "$gff_file"
            echo "$fa_file"; echo ""
            return 0
        fi
        local n_cds
        n_cds=$(grep -c $'\tCDS\t' "$gff_file" 2>/dev/null || echo 0)
        log_info "    GFF3 OK for $acc: $n_cds CDS feature(s)" >&2
    fi

    echo "$fa_file"
    echo "$gff_file"
}

# Translates iVar TSV variants to protein mutations using a GFF3 CDS map.
# Uses pure awk+bash — no Python dependency.
# Usage: _translate_ivar_to_protein IVAR_TSV REF_FA GFF3 SEG_LABEL SAMPLE TYPE
#        Outputs TSV rows: sample TYPE segment gene aa_pos ref_aa alt_aa freq depth mutation
_translate_ivar_to_protein() {
    # Reads the iVar TSV and extracts protein mutations directly from the
    # columns that iVar already computed when called with -g <GFF3>:
    #   col 15: GFF_FEATURE  (gene name, e.g. PB2:cds-YP_308849.1)
    #   col 17: REF_AA       (reference amino acid, single letter)
    #   col 19: ALT_AA       (alternative amino acid, single letter)
    #   col 20: POS_AA       (amino acid position)
    #   col 11: ALT_FREQ     (variant frequency)
    #   col 12: TOTAL_DP     (coverage depth)
    #   col 14: PASS         (TRUE/FALSE)
    # No manual translation needed — iVar + GFF3 already did it.
    local ivar_tsv="$1"
    local ref_fa="$2"    # kept for signature compatibility, not used
    local gff3="$3"      # kept for signature compatibility, not used
    local seg_label="$4"
    local sample="$5"
    local tipo="$6"

    [ -f "$ivar_tsv" ] && [ -s "$ivar_tsv" ] || return 0

    awk -F'\t' -v samp="$sample" -v tp="$tipo" -v seg="$seg_label" '
        NR == 1 { next }
        $14 != "TRUE" { next }
        {
            alt_freq = $11 + 0
            depth    = $12 + 0
            gff_feat = $15   # GFF_FEATURE: gene name or NA
            ref_aa   = $17   # REF_AA
            alt_aa   = $19   # ALT_AA
            pos_aa   = $20   # POS_AA

            # Extract short gene name from GFF_FEATURE
            # Format: "PB2:cds-YP_308849.1" → "PB2"
            gene = gff_feat
            if (index(gff_feat, ":") > 0)
                gene = substr(gff_feat, 1, index(gff_feat, ":") - 1)

            # Build mutation notation
            if (ref_aa == "NA" || ref_aa == "" || alt_aa == "NA" || alt_aa == "") {
                # Position outside CDS — UTR region, no aa annotation
                mutation = "."
                gene     = "UTR"
                pos_aa   = $2   # use nt position instead
                ref_aa   = $3   # REF nt
                alt_aa   = $4   # ALT nt
            } else if (ref_aa == alt_aa) {
                mutation = "."  # synonymous
            } else {
                mutation = ref_aa pos_aa alt_aa   # e.g. K189Q
            }

            freq_pct = sprintf("%.2f%%", alt_freq * 100)

            printf "%s\t%s\t%s\t%s\t%s\t%s\t%s\t%s\t%d\t%s\n",
                samp, tp, seg, gene, pos_aa, ref_aa, alt_aa,
                freq_pct, depth, mutation
        }
    ' "$ivar_tsv"
}

# Main function: run full-genome variant calling for one sample.
# Called from Step 10b.
# Usage: run_fullvarcall_sample SAMPLE READS_R1 READS_R2 TIPO SUBTIPO_HA SUBTIPO_NA IRMA_DIR
run_fullvarcall_sample() {
    local sample="$1"
    local reads_r1="$2"
    local reads_r2="$3"
    local tipo="$4"
    local sub_ha="$5"
    local sub_na="$6"
    local irma_dir="$7"

    local out_dir="$FULLVARCALL_DIR/$sample"
    mkdir -p "$out_dir"

    local mm_preset="sr"
    [ "$IS_LONGREAD" = "yes" ] && mm_preset="map-ont"

    local segments_done=0
    local segments_skip=0

    # Get all 8 NC_* accessions for this type/subtype.
    # Segment identity (1-8) is determined by _seg_from_gff3 from the GFF3 gene content,
    # not from the position in the list — works correctly for FluA and FluB.
    local all_accs
    all_accs=$(_get_refseq_accs "$tipo" "$sub_ha")
    if [ -z "$all_accs" ]; then
        log_warn "  No RefSeq accessions for type=$tipo subtype_HA=$sub_ha. Skipping step 10b."
        return 0
    fi

    for acc in $all_accs; do

        # Download/retrieve RefSeq FASTA and GFF3 (cached)
        local refseq_info
        mapfile -t refseq_info < <(_ensure_refseq "$acc")
        local refseq_fa="${refseq_info[0]:-}"
        local refseq_gff="${refseq_info[1]:-}"

        if [ -z "$refseq_fa" ] || [ ! -s "$refseq_fa" ]; then
            log_warn "    RefSeq FASTA unavailable for $acc. Skipping."
            segments_skip=$((segments_skip + 1))
            continue
        fi

        # Determine segment number and name from the GFF3 gene annotations.
        # This works for FluA (H1N1, H3N2, H5N1) and FluB without any hardcoded ordering.
        local seg_gff_result=""
        if [ -n "$refseq_gff" ] && [ -s "$refseq_gff" ]; then
            seg_gff_result=$(_seg_from_gff3 "$refseq_gff")
        fi
        if [ -z "$seg_gff_result" ]; then
            log_warn "    Cannot identify segment from GFF3 for $acc. Skipping." >&2
            segments_skip=$((segments_skip + 1))
            continue
        fi
        local segnum segname
        segnum=$(echo "$seg_gff_result" | awk '{print $1}')
        segname=$(echo "$seg_gff_result" | awk '{print $2}')
        local seg_label="${sample}_${tipo}_${segnum}"
        log_info "    Seg ${segnum} (${segname}): aligning → $acc"

        # Build aligner index for this RefSeq (cached — built once per accession)
        # Short reads: Bowtie2 index; Long reads (ONT): minimap2 .mmi index
        if [ "$IS_LONGREAD" = "no" ]; then
            if [ ! -f "${refseq_fa}.1.bt2" ] && [ ! -f "${refseq_fa}.1.bt2l" ]; then
                log_info "    Building Bowtie2 index for $acc..."
                $CONDA_RUN bowtie2-build                     --threads "$(nproc --all)"                     "$refseq_fa" "$refseq_fa"                     >> "$run_log_file" 2>&1 || true
            fi
        else
            local refseq_idx="${refseq_fa}.mmi"
            if [ ! -f "$refseq_idx" ]; then
                $CONDA_RUN minimap2 -d "$refseq_idx" "$refseq_fa"                     >> "$run_log_file" 2>&1 || rm -f "$refseq_idx"
            fi
        fi

        local bam_out="$out_dir/${sample}_${segname}_refseq.bam"
        local ivar_prefix="$out_dir/${seg_label}_ivar"

        # Skip if already processed (supports resume within step)
        if [ -f "${ivar_prefix}.tsv" ] && [ -s "${ivar_prefix}.tsv" ]; then
            log_skip "    Seg ${segnum} (${segname}) already processed for $sample."
        else
            # Alignment: Bowtie2 for short reads (Illumina/BGI), minimap2 for ONT
            if [ "$IS_LONGREAD" = "no" ]; then
                if [ -n "$reads_r2" ] && [ -f "$reads_r2" ]; then
                    # Paired-end: extract mapped reads, then produce sorted BAM
                    $CONDA_RUN bowtie2                         -x "$refseq_fa"                         -1 "$reads_r1" -2 "$reads_r2"                         -p "$(nproc --all)"                         --very-sensitive                         --no-unal                         -S /dev/null                         --al-conc-gz "$out_dir/${sample}_${segname}_mapped_R%.fastq.gz"                         2>> "$run_log_file" || true
                    local mapped_r1="$out_dir/${sample}_${segname}_mapped_R1.fastq.gz"
                    local mapped_r2="$out_dir/${sample}_${segname}_mapped_R2.fastq.gz"
                    if [ -s "$mapped_r1" ]; then
                        $CONDA_RUN bowtie2                             -x "$refseq_fa"                             -1 "$mapped_r1" -2 "$mapped_r2"                             -p "$(nproc --all)"                             --very-sensitive                             2>> "$run_log_file"                         | $CONDA_RUN samtools sort -o "$bam_out" -                             >> "$run_log_file" 2>&1 || true
                    fi
                else
                    # Single-end
                    $CONDA_RUN bowtie2                         -x "$refseq_fa"                         -U "$reads_r1"                         -p "$(nproc --all)"                         --very-sensitive                         2>> "$run_log_file"                     | $CONDA_RUN samtools sort -o "$bam_out" -                         >> "$run_log_file" 2>&1 || true
                fi
            else
                # minimap2 map-ont for ONT long reads
                $CONDA_RUN minimap2                     -ax map-ont                     -t "$(nproc --all)"                     "$refseq_fa" "$reads_r1"                     2>> "$run_log_file"                 | $CONDA_RUN samtools sort -o "$bam_out" -                     >> "$run_log_file" 2>&1 || true
            fi

            if [ ! -s "$bam_out" ]; then
                log_warn "    Seg ${segnum}: alignment produced empty BAM. Skipping."
                segments_skip=$((segments_skip + 1))
                continue
            fi

            $CONDA_RUN samtools index "$bam_out" >> "$run_log_file" 2>&1 || true

            # iVar variants
            $CONDA_RUN samtools mpileup \
                -A -d 0 -B -Q 0 \
                --reference "$refseq_fa" \
                "$bam_out" 2>> "$run_log_file" \
            | $CONDA_RUN ivar variants \
                -p "$ivar_prefix" \
                -q 20 \
                -t "$IVAR_FREQ" \
                -m "$IVAR_DEPTH" \
                -r "$refseq_fa" \
                -g "$refseq_gff" \
                >> "$run_log_file" 2>&1 || true

            # Fallback: iVar without GFF3 if GFF3-mode fails or GFF absent
            if [ ! -s "${ivar_prefix}.tsv" ]; then
                log_warn "    iVar with GFF3 failed for seg ${segnum}. Retrying without GFF3."
                $CONDA_RUN samtools mpileup \
                    -A -d 0 -B -Q 0 \
                    --reference "$refseq_fa" \
                    "$bam_out" 2>> "$run_log_file" \
                | $CONDA_RUN ivar variants \
                    -p "$ivar_prefix" \
                    -q 20 \
                    -t "$IVAR_FREQ" \
                    -m "$IVAR_DEPTH" \
                    -r "$refseq_fa" \
                    >> "$run_log_file" 2>&1 || true
            fi
        fi

        # Translate iVar output to protein mutations
        if [ -s "${ivar_prefix}.tsv" ]; then
            local protein_tsv="$out_dir/${seg_label}_protein_mutations.tsv"
            if [ ! -s "$protein_tsv" ]; then
                {
                    echo -e "sample\ttype\tsegment\tgene\taa_position\tref_aa\talt_aa\tfrequency\tdepth\tmutation"
                    _translate_ivar_to_protein \
                        "${ivar_prefix}.tsv" \
                        "$refseq_fa" \
                        "$refseq_gff" \
                        "$seg_label" \
                        "$sample" \
                        "$tipo"
                } > "$protein_tsv" 2>/dev/null || true
            fi

            local n_var
            n_var=$(awk 'NR>1 && $10!="." {n++} END{print n+0}' "$protein_tsv" 2>/dev/null || echo 0)
            log_info "    → Seg ${segnum} (${segname}): $(tail -n +2 "${ivar_prefix}.tsv" | wc -l) nt variants, $n_var aa changes"
            segments_done=$((segments_done + 1))
        else
            log_warn "    Seg ${segnum}: iVar produced no output."
            segments_skip=$((segments_skip + 1))
        fi
    done

    log_info "  Full varcall $sample: $segments_done segment(s) done, $segments_skip skipped."
    echo "$segments_done"
}

# Aggregate all per-sample protein mutation TSVs into one master table.
# Usage: aggregate_protein_mutations OUTPUT_DIR → prints path of aggregate TSV
aggregate_protein_mutations() {
    local base_dir="$1"
    local agg_tsv="$base_dir/all_samples_protein_mutations.tsv"

    echo -e "sample\ttype\tsegment\tgene\taa_position\tref_aa\talt_aa\tfrequency\tdepth\tmutation" \
        > "$agg_tsv"

    local count=0
    for pm_tsv in "$base_dir"/*/  ; do
        local sample_dir="$pm_tsv"
        for f in "$sample_dir"*_protein_mutations.tsv; do
            [ -f "$f" ] && [ -s "$f" ] || continue
            tail -n +2 "$f" >> "$agg_tsv" && count=$((count + 1))
        done
    done

    log_info "  Aggregated $count protein mutation file(s) → $agg_tsv"
    echo "$agg_tsv"
}


# ── samtools depth on IRMA BAMs ─────────────────────────────────────────────
run_samtools_depth() {
    local sample="$1"
    local sample_irma_dir="$2"
    local depth_out_dir="$3"

    mkdir -p "$depth_out_dir/$sample"

    # BAMs are in the root of the IRMA sample directory
    local bam_found=0
    for bam in "$sample_irma_dir"/*.bam; do
        [ -f "$bam" ] || continue
        local seg_name
        seg_name=$(basename "$bam" .bam)
        local depth_file="$depth_out_dir/$sample/${seg_name}.depth.tsv"

        # samtools depth: -a includes positions with depth 0, -d 0 removes depth limit
        $CONDA_RUN samtools depth -a -d 0 "$bam" > "$depth_file" 2>> "$run_log_file" || true

        if [ -s "$depth_file" ]; then
            bam_found=$((bam_found + 1))
        fi
    done

    # Also checks intermediate/ subfolder if present
    for bam in "$sample_irma_dir"/intermediate/*.bam "$sample_irma_dir"/secondary/*.bam; do
        [ -f "$bam" ] || continue
        local seg_name
        seg_name=$(basename "$bam" .bam)
        local depth_file="$depth_out_dir/$sample/${seg_name}_secondary.depth.tsv"
        $CONDA_RUN samtools depth -a -d 0 "$bam" > "$depth_file" 2>> "$run_log_file" || true
    done

    echo "$bam_found"
}

# ── Precise coverage via samtools depth ─────────────────────────────────────
compute_depth_stats() {
    local depth_file="$1"
    # Returns: mean|min|max|covered_positions|ref_length
    awk '
    BEGIN { sum=0; n=0; min=999999; max=0; covered=0 }
    {
        depth=$3+0
        sum += depth
        n++
        if (depth < min) min = depth
        if (depth > max) max = depth
        if (depth > 0) covered++
    }
    END {
        if (n > 0)
            printf "%.1f|%d|%d|%d|%d", sum/n, min, max, covered, n
        else
            print "0|0|0|0|0"
    }' "$depth_file" 2>/dev/null || echo "0|0|0|0|0"
}

# ── iVar variant calling (short reads) ──────────────────────────────────────
# Correct flow for antiviral resistance:
#   post-QC reads → minimap2 → canonical BAM → samtools mpileup | ivar variants
# Variant positions are in canonical WHO/FluSurver coordinates,
# enabling correct identification of H275Y, I38T etc.
run_ivar_variants() {
    local sample="$1"     # sample name
    local reads_r1="$2"   # reads post-QC (trimmed/depleted)
    local reads_r2="$3"   # R2 ou vazio para single-end
    local subtipo_ha="$4" # ex: H1, H3, H5
    local subtipo_na="$5" # ex: N1, N2
    local out_dir="$6"

    mkdir -p "$out_dir/$sample"

    if ! conda_has_tool "ivar"; then
        log_warn "  iVar not found in environment '$CONDA_ENV_MKFLU'. Skipping variant calling."
        echo "0"
        return 1
    fi
    if ! conda_has_tool "minimap2"; then
        log_warn "  minimap2 not found. Skipping iVar."
        echo "0"
        return 1
    fi

    local called=0

    # Select subtype key: N1 for H1N1/H5N1/others, N2 for H3N2
    # Without declare -A: uses simple variables for subshell $() compatibility
    local na_upper="${subtipo_na^^}"
    local subtype_key="N1"
    [[ "$na_upper" == N2* ]] && subtype_key="N2"

    local ref_na="$CANONICAL_REFS_DIR/${subtype_key}_NA.fa"
    local ref_pa="$CANONICAL_REFS_DIR/${subtype_key}_PA.fa"
    local ref_mp="$CANONICAL_REFS_DIR/${subtype_key}_MP.fa"

    # Check if at least one reference exists
    if [ ! -f "$ref_na" ] && [ ! -f "$ref_pa" ] && [ ! -f "$ref_mp" ]; then
        log_warn "  No canonical reference available for $sample (subtype: $subtype_key)."
        log_warn "  Run the pipeline with internet access to download references automatically."
        log_warn "  Expected directory: $CANONICAL_REFS_DIR"
        echo "0"
        return 0
    fi

    # Iterate over three genes: NA, PA, MP
    for gene in NA PA MP; do
        local ref_fa=""
        case "$gene" in
            NA) ref_fa="$ref_na" ;;
            PA) ref_fa="$ref_pa" ;;
            MP) ref_fa="$ref_mp" ;;
        esac
        local prefix="$out_dir/$sample/${sample}_${gene}_canonical"
        local canon_bam="${prefix}.bam"
        local ivar_prefix="${prefix}_ivar"

        log_info "    iVar ${gene}: aligning against canonical reference..."

        # minimap2 alignment against canonical reference
        # -ax sr = short reads paired/single; -ax map-ont = ONT
        local mm_preset="sr"
        [ "$IS_LONGREAD" = "yes" ] && mm_preset="map-ont"

        if [ -n "$reads_r2" ] && [ -f "$reads_r2" ]; then
            $CONDA_RUN minimap2 -ax "$mm_preset" -t "$(nproc --all)" \
                "$ref_fa" "$reads_r1" "$reads_r2" 2>> "$run_log_file" \
            | $CONDA_RUN samtools sort -o "$canon_bam" - >> "$run_log_file" 2>&1 || true
        else
            $CONDA_RUN minimap2 -ax "$mm_preset" -t "$(nproc --all)" \
                "$ref_fa" "$reads_r1" 2>> "$run_log_file" \
            | $CONDA_RUN samtools sort -o "$canon_bam" - >> "$run_log_file" 2>&1 || true
        fi

        [ -f "$canon_bam" ] && [ -s "$canon_bam" ] || continue
        $CONDA_RUN samtools index "$canon_bam" >> "$run_log_file" 2>&1 || true

        # samtools mpileup | ivar variants
        # Note: without -g GFF the amino acid columns (15-19) are empty,
        # but POS, ALT, ALT_FREQ, TOTAL_DP and PASS (cols 2,4,11,12,14) are filled.
        $CONDA_RUN samtools mpileup \
            -A -d 0 -B -Q 0 \
            --reference "$ref_fa" \
            "$canon_bam" 2>> "$run_log_file" \
        | $CONDA_RUN ivar variants \
            -p "$ivar_prefix" \
            -q 20 \
            -t "$IVAR_FREQ" \
            -m "$IVAR_DEPTH" \
            -r "$ref_fa" \
            >> "$run_log_file" 2>&1 || true

        if [ -f "${ivar_prefix}.tsv" ] && [ -s "${ivar_prefix}.tsv" ]; then
            called=$((called + 1))
            local n_var
            n_var=$(tail -n +2 "${ivar_prefix}.tsv" | wc -l)
            if [ "$n_var" -gt 0 ]; then
                log_info "      → ${gene}: $n_var variant(s) detected at resistance positions"
            else
                log_info "      → ${gene}: no variants at monitored positions (no resistance detected)"
            fi
        fi
    done

    echo "$called"
}


# ── Medaka variant calling (ONT) ────────────────────────────────────────────
# Uses the separate conda environment CONDA_ENV_MEDAKA (default: medaka_env),
# since Medaka has dependencies incompatible with the mk_flu environment.
run_medaka_variants() {
    local sample="$1"
    local sample_irma_dir="$2"
    local out_dir="$3"

    mkdir -p "$out_dir/$sample"

    if [ -z "$MEDAKA_RUN" ]; then
        log_warn "  MEDAKA_RUN not configured (environment '${CONDA_ENV_MEDAKA}' missing). Skipping."
        echo "0"
        return 1
    fi

    local called=0
    for bam in "$sample_irma_dir"/*.bam; do
        [ -f "$bam" ] || continue
        local seg_name
        seg_name=$(basename "$bam" .bam)
        local ref_fa="$sample_irma_dir/amended_consensus/${seg_name}.fa"
        [ -f "$ref_fa" ] || continue

        local medaka_out="$out_dir/$sample/${seg_name}_medaka"
        mkdir -p "$medaka_out"

        # Sorts and indexes BAM if needed
        local sorted_bam="${bam%.bam}.sorted.bam"
        if [ ! -f "$sorted_bam" ]; then
            $CONDA_RUN samtools sort -o "$sorted_bam" "$bam" >> "$run_log_file" 2>&1 || true
            $CONDA_RUN samtools index "$sorted_bam" >> "$run_log_file" 2>&1 || true
        fi
        [ -f "$sorted_bam" ] || sorted_bam="$bam"

        # Medaka runs in the separate medaka_env conda environment
        $MEDAKA_RUN medaka_haploid_variant \
            -i "$sorted_bam" \
            -r "$ref_fa" \
            -o "$medaka_out" \
            -t "$(nproc --all)" \
            >> "$run_log_file" 2>&1 || true

        [ -f "$medaka_out/medaka.annotated.vcf" ] && called=$((called + 1))
        log_info "    Medaka (${CONDA_ENV_MEDAKA}): $seg_name → $medaka_out"
    done
    echo "$called"
}

# ── Co-infection via allAlleles.txt ─────────────────────────────────────────
# Reads IRMA allAlleles file and flags positions with minority allele
# above threshold. If % of flagged positions > COINFECTION_PCT → WARN.
# IRMA allAlleles.txt columns:
#   Reference_Name | Position | Allele | Count | Total | Frequency | ... | Allele_Type
# Strategy: group by position; if sum of minority frequencies >= MINORITY_FREQ_MIN
# in >= COINFECTION_PCT% of positions → emit WARN for possible co-infection/mixing.
analyze_coinfection() {
    local sample="$1"
    local sample_irma_dir="$2"
    local min_freq="$3"    # e.g. 0.20 (minority allele frequency threshold)
    local pct_thresh="$4"  # e.g. 5.0 (% positions)

    local status="OK"
    local details=""
    local total_segs=0
    local flagged_segs=0

    for allele_file in "$sample_irma_dir"/tables/*-allAlleles.txt; do
        [ -f "$allele_file" ] || continue
        local seg_name
        seg_name=$(basename "$allele_file" -allAlleles.txt)
        total_segs=$((total_segs + 1))

        # Estrutura: Reference_Name Position Allele Count Total Frequency ... Allele_Type
        # Agrupa por posição somando frequências de linhas com Allele_Type == "Minority"
        # Positions with Total < 10 are ignored (insufficient coverage)
        local result
        result=$(awk -F'\t' -v mf="$min_freq" '
            NR == 1 {
                for (i=1; i<=NF; i++) {
                    if ($i == "Position")    col_pos  = i
                    if ($i == "Frequency")   col_freq = i
                    if ($i == "Total")       col_tot  = i
                    if ($i == "Allele_Type") col_type = i
                }
                next
            }
            NR > 1 && col_pos && col_freq && col_type {
                pos   = $col_pos
                freq  = $col_freq + 0
                tot   = $col_tot  + 0
                atype = $col_type
                if (tot < 10) next
                all_pos[pos] = tot
                if (atype == "Minority") minor_sum[pos] += freq
            }
            END {
                total = 0; flagged = 0
                for (p in all_pos) {
                    total++
                    if (minor_sum[p] + 0 >= mf) flagged++
                }
                if (total > 0)
                    printf "%d|%d|%.2f", flagged, total, (flagged/total)*100
                else
                    print "0|0|0.00"
            }
        ' "$allele_file" 2>/dev/null || echo "0|0|0.00")

        local flagged_pos total_pos flag_pct
        flagged_pos="${result%%|*}"
        local rest="${result#*|}"
        total_pos="${rest%%|*}"
        flag_pct="${rest##*|}"

        local above_thresh
        above_thresh=$(awk "BEGIN { print ($flag_pct >= $pct_thresh) ? 1 : 0 }")
        if [ "$above_thresh" = "1" ]; then
            flagged_segs=$((flagged_segs + 1))
            details="${details}${seg_name}(${flagged_pos}/${total_pos}pos,${flag_pct}%) "
            status="WARN"
        fi
    done

    echo "${status}|segs_analyzed:${total_segs}|segs_flagged:${flagged_segs}|${details:-no_alert}"
}


# ── Antiviral resistance analysis via mpileup ───────────────────────────────
# ── Resistance analysis: cross-references canonical iVar output with WHO database ─
# Reads *_ivar.tsv files generated by run_ivar_variants and filters by
# positions of interest from flu_antiviral_markers.tsv database (FluSurver/WHO).
# Only reports variants matching known resistance positions.
# ── Resistance analysis: cross-references canonical iVar 1.4.x output with WHO ──
# iVar 1.4.4 TSV tem 19 colunas:
#   1:REGION 2:POS 3:REF 4:ALT 5:REF_DP 6:REF_RV 7:REF_QUAL
#   8:ALT_DP 9:ALT_RV 10:ALT_QUAL 11:ALT_FREQ 12:TOTAL_DP 13:PVAL 14:PASS
#   15:GFF_FEATURE 16:REF_CODON 17:REF_AA 18:ALT_CODON 19:ALT_AA
# Usamos awk para extrair exatamente as colunas corretas independente do número total.
# ── Codon to amino acid translation (awk inline) ────────────────────────────
# Extracts majority base from a mpileup line and returns as uppercase letter.
# Usage: get_codon_aa BAM REF_FA REF_NAME NT_POS CONDA_RUN → "GAA" or "N/D"
get_codon_from_bam() {
    local bam="$1"
    local ref_fa="$2"
    local ref_name="$3"
    local nt_pos="$4"
    local nt_end=$(( nt_pos + 2 ))

    # Extrai as 3 posições do códon via mpileup e pega base majoritária por posição
    $CONDA_RUN samtools mpileup \
        -r "${ref_name}:${nt_pos}-${nt_end}" \
        -f "$ref_fa" \
        -A -d 0 -Q 20 \
        "$bam" 2>/dev/null \
    | awk '
        {
            bases = $5
            gsub(/\^./, "", bases)
            gsub(/\$/, "", bases)
            gsub(/[+-][0-9]+[ACTGNacgtn]+/, "", bases)
            gsub(/[*<>]/, "", bases)
            n = split(toupper(bases), arr, "")
            delete count
            for (i=1; i<=n; i++) {
                b = arr[i]
                if (b ~ /[ACGT]/) count[b]++
            }
            max = 0; best = "N"
            for (b in count) if (count[b] > max) { max = count[b]; best = b }
            total = 0
            for (b in count) total += count[b]
            if (total == 0) best = "N"
            printf "%s", best
        }
        END { printf "\n" }
    ' 2>/dev/null || echo "NNN"
}

translate_codon_awk() {
    local codon="$1"
    echo "$codon" | awk '
    function tr(c,    t) {
        t["TTT"]="F";t["TTC"]="F";t["TTA"]="L";t["TTG"]="L"
        t["CTT"]="L";t["CTC"]="L";t["CTA"]="L";t["CTG"]="L"
        t["ATT"]="I";t["ATC"]="I";t["ATA"]="I";t["ATG"]="M"
        t["GTT"]="V";t["GTC"]="V";t["GTA"]="V";t["GTG"]="V"
        t["TCT"]="S";t["TCC"]="S";t["TCA"]="S";t["TCG"]="S"
        t["CCT"]="P";t["CCC"]="P";t["CCA"]="P";t["CCG"]="P"
        t["ACT"]="T";t["ACC"]="T";t["ACA"]="T";t["ACG"]="T"
        t["GCT"]="A";t["GCC"]="A";t["GCA"]="A";t["GCG"]="A"
        t["TAT"]="Y";t["TAC"]="Y";t["TAA"]="*";t["TAG"]="*"
        t["CAT"]="H";t["CAC"]="H";t["CAA"]="Q";t["CAG"]="Q"
        t["AAT"]="N";t["AAC"]="N";t["AAA"]="K";t["AAG"]="K"
        t["GAT"]="D";t["GAC"]="D";t["GAA"]="E";t["GAG"]="E"
        t["TGT"]="C";t["TGC"]="C";t["TGA"]="*";t["TGG"]="W"
        t["CGT"]="R";t["CGC"]="R";t["CGA"]="R";t["CGG"]="R"
        t["AGT"]="S";t["AGC"]="S";t["AGA"]="R";t["AGG"]="R"
        t["GGT"]="G";t["GGC"]="G";t["GGA"]="G";t["GGG"]="G"
        c = toupper(c)
        if (c in t) return t[c]
        return "?"
    }
    { print tr($0) }
    '
}

# ── Antiviral resistance: reads iVar TSV + translates codon to aa nomenclature ─
analyze_antiviral_resistance() {
    local sample="$1"
    local varcall_sample_dir="$2"
    local tipo="$3"
    local subtipo_ha="$4"
    local subtipo_na="$5"

    if [ ! -f "$ANTIVIRAL_TSV" ]; then
        echo "no_database"
        return
    fi

    local na_upper="${subtipo_na^^}"
    local subtype_key="N1"
    [[ "$na_upper" == N2* ]] && subtype_key="N2"

    local report_lines=""
    local found=0

    for ivar_tsv in "$varcall_sample_dir"/*_canonical_ivar.tsv; do
        [ -f "$ivar_tsv" ] && [ -s "$ivar_tsv" ] || continue

        local fname
        fname=$(basename "$ivar_tsv" _canonical_ivar.tsv)   # → SAMPLE_GENE
        local gene="${fname##*_}"                             # → GENE (NA, PA, MP)
        local gene_bank="$gene"
        [ "$gene_bank" = "MP" ] && gene_bank="M2"

        # Localiza o BAM canônico correspondente para extração de códon
        local canon_bam
        canon_bam=$(ls "$varcall_sample_dir"/${sample}_${gene}_canonical.bam 2>/dev/null | head -1 || true)
        local canon_ref
        canon_ref=$(ls "$CANONICAL_REFS_DIR"/${subtype_key}_${gene}.fa 2>/dev/null | head -1 || true)
        [ "$gene_bank" = "M2" ] && canon_ref=$(ls "$CANONICAL_REFS_DIR"/${subtype_key}_MP.fa 2>/dev/null | head -1 || true)

        local variants_file
        variants_file=$(mktemp)

        awk -F'\t' -v freq_min="$IVAR_FREQ" '
            NR == 1 { next }
            $14 == "TRUE" && ($11 + 0) >= freq_min {
                printf "%s\t%s\t%s\t%s\n", $2, $4, $11, $12
            }
        ' "$ivar_tsv" > "$variants_file" 2>/dev/null

        while IFS=$'\t' read -r pos alt alt_freq total_dp; do
            local aa_pos=$(( (pos - 1) / 3 + 1 ))

            while IFS=$'\t' read -r bk_gene bk_subtipo bk_codon bk_wt bk_mut bk_drug bk_sig; do
                [[ "$bk_gene" == "#"* ]] && continue
                [ "$bk_gene" = "gene" ]  && continue
                [ -z "$bk_gene" ]        && continue
                [ "$bk_gene" = "$gene_bank" ]  || continue
                [ "$bk_codon" = "$aa_pos" ]    || continue
                [ "$bk_subtipo" = "ALL" ] || [ "$bk_subtipo" = "$subtype_key" ] || continue

                local freq_pct
                freq_pct=$(awk "BEGIN{printf \"%.2f%%\", $alt_freq * 100}")

                # Traduz códon observado para aminoácido
                local obs_aa="?"
                local nomenclature="?"
                if [ -f "$canon_bam" ] && [ -f "$canon_ref" ]; then
                    local ref_name
                    ref_name=$(head -1 "$canon_ref" | sed 's/>//' | awk '{print $1}')
                    local nt_pos=$(( (aa_pos - 1) * 3 + 1 ))
                    local codon
                    codon=$(get_codon_from_bam "$canon_bam" "$canon_ref" "$ref_name" "$nt_pos")
                    obs_aa=$(translate_codon_awk "$codon")
                    if [ "$obs_aa" != "?" ] && [ "$obs_aa" != "N" ]; then
                        nomenclature="${bk_wt}${aa_pos}${obs_aa}"
                    else
                        nomenclature="${bk_wt}${aa_pos}?"
                        obs_aa="N/D"
                    fi
                else
                    nomenclature="${bk_wt}${aa_pos}?"
                    obs_aa="N/D"
                fi

                # Only report when amino acid could be determined (complete nomenclature)
                [ "$obs_aa" = "N/D" ] && continue

                report_lines="${report_lines}${sample}\t${gene_bank}\t${aa_pos}\t${bk_wt}\t${bk_mut}\t${obs_aa}\t${freq_pct}\t${total_dp}\t${bk_drug}\t${bk_sig}\t${nomenclature}\n"
                found=$((found + 1))
            done < <(grep -v '^#' "$ANTIVIRAL_TSV" | tail -n +2)

        done < "$variants_file"
        rm -f "$variants_file"
    done

    if [ "$found" -gt 0 ]; then
        printf "%b" "$report_lines"
    else
        echo "no_data"
    fi
}




# ── H5 virulence markers ─────────────────────────────────────────────────────
# ── H5 virulence markers: extracts codon and translates to aa nomenclature ───
analyze_h5_virulence() {
    local sample="$1"
    local sample_irma_dir="$2"
    local out_file="$3"

    log_info "  Analyzing H5 virulence markers: $sample"

    # Markers: gene_pos → "wt|mut|description"
    declare -A H5_MARKERS=(
        ["PB2_627"]="E|K|Mammalian adaptation — enhanced replication at 33°C"
        ["PB2_701"]="D|N|Mammalian adaptation — enhanced replication"
        ["PA_97"]="I|V|Increased virulence in mammals"
        ["PB1_66"]="N|S|Increased virulence and pathogenicity"
        ["NS1_92"]="D|E|Interferon resistance"
    )

    {
        echo -e "sample\tgene\taa_position\twt_expected\tmut_reference\tobserved_aa\tdescription\tnomenclature\tstatus"

        for marker_key in "${!H5_MARKERS[@]}"; do
            local gene aa_pos
            gene="${marker_key%%_*}"
            aa_pos="${marker_key##*_}"
            local wt mut description
            wt="${H5_MARKERS[$marker_key]%%|*}"
            local rest="${H5_MARKERS[$marker_key]#*|}"
            mut="${rest%%|*}"
            description="${rest##*|}"

            # Finds BAM and IRMA reference for this gene
            local bam ref_fa
            bam=$(ls "$sample_irma_dir"/*${gene}*.bam 2>/dev/null | head -1 || true)
            ref_fa=$(ls "$sample_irma_dir"/*${gene}*.fasta 2>/dev/null | head -1 || true)
            [ -f "$bam" ] && [ -f "$ref_fa" ] || continue

            local ref_name
            ref_name=$(head -1 "$ref_fa" | sed 's/>//' | awk '{print $1}')
            local nt_pos=$(( (aa_pos - 1) * 3 + 1 ))

            # Extracts codon and translates
            local codon
            codon=$(get_codon_from_bam "$bam" "$ref_fa" "$ref_name" "$nt_pos")
            local obs_aa
            obs_aa=$(translate_codon_awk "$codon")

            # Determine status and nomenclature
            local marker_status nomenclature
            if [ "$obs_aa" = "?" ] || [ "$obs_aa" = "N" ]; then
                # Insufficient coverage — skip, do not report
                continue
            elif [ "$obs_aa" = "$wt" ]; then
                marker_status="WT"
                nomenclature="${wt}${aa_pos}${wt}"
            elif [ "$obs_aa" = "$mut" ]; then
                marker_status="MUT_DETECTED"
                nomenclature="${wt}${aa_pos}${mut}"
            else
                marker_status="VARIANT"
                nomenclature="${wt}${aa_pos}${obs_aa}"
            fi

            echo -e "${sample}\t${gene}\t${aa_pos}\t${wt}\t${mut}\t${obs_aa}\t${description}\t${nomenclature}\t${marker_status}"
        done
    } > "$out_file" 2>/dev/null || true
}


# ── GISAID-ready FASTA and CSV generation ───────────────────────────────────
generate_gisaid_outputs() {
    local typing_tsv="$1"
    local consensus_fasta="$2"
    local out_dir="$3"
    local location="$4"
    local year="$5"

    mkdir -p "$out_dir"

    local gisaid_fasta="$out_dir/gisaid_sequences.fasta"
    local gisaid_csv="$out_dir/gisaid_metadata.csv"

    # EpiFlu CSV header
    echo "Isolate_Id,Isolate_Name,Subtype,Lineage,Passage_History,Location,Host,Originating_Lab,Submitting_Lab,Authors,Submission_Date,Collection_Date,Sequence_Length" \
        > "$gisaid_csv"

    > "$gisaid_fasta"

    while IFS=$'\t' read -r sample tipo subtipo_ha subtipo_na classif clado qc_nc fonte hit_ha hit_na segs qc_asm qc_det; do
        [ "$sample" = "sample" ] || [ "$sample" = "amostra" ] && continue
        [ -z "$sample" ] && continue

        # Build GISAID name: A/location/code/year(HxNy)
        local virus_name subtype_str
        local loc="${location:-Brasil}"
        if [ "$tipo" = "A" ]; then
            if [ "$subtipo_ha" != "nd" ] && [ "$subtipo_na" != "nd" ]; then
                subtype_str="${subtipo_ha}${subtipo_na}"
                virus_name="A/${loc}/${sample}/${year}(${subtype_str})"
            else
                subtype_str="${classif:-H?N?}"
                virus_name="A/${loc}/${sample}/${year}"
            fi
        elif [ "$tipo" = "B" ]; then
            subtype_str="B"
            if echo "$classif" | grep -qi "victoria"; then
                virus_name="B/${loc}/${sample}/${year}(Victoria)"
            elif echo "$classif" | grep -qi "yamagata"; then
                virus_name="B/${loc}/${sample}/${year}(Yamagata)"
            else
                virus_name="B/${loc}/${sample}/${year}"
            fi
        else
            virus_name="${tipo:-A}/${loc}/${sample}/${year}"
            subtype_str="${classif:-desconhecido}"
        fi

        # Generate FASTA with per-segment header in format: SAMPLE_TYPE_SEGNUM
        # E.g.: ERR3474709_B_1, ERR3474709_B_2 ... ERR3474709_B_8
        # The multisample_consensus.fasta has IRMA headers such as:
        #   >SAMPLE_1, >SAMPLE_2 ... >SAMPLE_8  (ou >SAMPLE_HA, >SAMPLE_NA etc)
        # We extract the segment number from the original header and build the new one.
        if [ -f "$consensus_fasta" ]; then
            awk -v samp="$sample" -v tipo="$tipo" '
                BEGIN { seg_counter = 0 }
                /^>/ {
                    keep = ($0 ~ samp)
                    if (keep) {
                        # Extrai número do segmento do header IRMA
                        # Formatos possíveis: >SAMPLE_1 .. >SAMPLE_8
                        #                    >SAMPLE_B_HA, >SAMPLE_A_NA_N1 etc
                        header = $0
                        sub(/^>/, "", header)
                        # Remove sample prefix + underscore
                        gsub("^" samp "_", "", header)
                        # Tenta pegar dígito 1-8 no início ou após underscore
                        segnum = ""
                        if (header ~ /^[1-8](_|$)/) {
                            # Header começa com número de segmento: >SAMPLE_1
                            segnum = substr(header, 1, 1)
                        } else if (match(header, /_[1-8](_|$)/)) {
                            # Número de segmento no meio/fim: >SAMPLE_X_2
                            segnum = substr(header, RSTART+1, 1)
                        } else {
                            # Mapping by gene name (IRMA long name format)
                            if      (header ~ /PB2/) segnum = 1
                            else if (header ~ /PB1/) segnum = 2
                            else if (header ~ /PA/)  segnum = 3
                            else if (header ~ /HA/)  segnum = 4
                            else if (header ~ /NP/)  segnum = 5
                            else if (header ~ /NA/)  segnum = 6
                            else if (header ~ /MP/)  segnum = 7
                            else if (header ~ /NS/)  segnum = 8
                            else { seg_counter++; segnum = seg_counter }
                        }
                        print ">" samp "_" tipo "_" segnum
                    }
                    next
                }
                keep { print }
            ' "$consensus_fasta" >> "$gisaid_fasta"
        fi

        # Linha CSV
        echo "${sample},${virus_name},${subtype_str},${clade:-},Original,${loc},Human,LACEN-AL,LACEN-AL,Jean Nascimento et al.,$(date +%Y-%m-%d),,," \
            >> "$gisaid_csv"

    done < "$typing_tsv"

    log_info "  GISAID FASTA  : $gisaid_fasta"
    log_info "  GISAID CSV    : $gisaid_csv"
}

# --------------------------------------------------------------------------- #
# Argument parsing (support for named flags)
# --------------------------------------------------------------------------- #

parse_args() {
    # Required positional arguments
    input_dir="${1:-}"
    output_dir="${2:-}"
    irma_module="${3:-}"
    forced_mode="${4:-}"

    # Named flags (may follow positional args or appear in any position)
    local i=1
    while [ $i -le $# ]; do
        local arg="${!i}"
        local next_i=$((i+1))
        local next_val="${!next_i:-}"
        case "$arg" in
            --adapter_fasta)  ADAPTER_FASTA="$next_val";   i=$((i+2)) ;;
            --min_len)        MIN_LEN_ILLUMINA="$next_val"; MIN_LEN_LONG="$next_val"; i=$((i+2)) ;;
            --min_len_short)  MIN_LEN_ILLUMINA="$next_val"; i=$((i+2)) ;;
            --min_len_long)   MIN_LEN_LONG="$next_val";    i=$((i+2)) ;;
            --max_len_long)   MAX_LEN_LONG="$next_val";    i=$((i+2)) ;;
            --min_qual)       MIN_QUAL="$next_val";         i=$((i+2)) ;;
            --host_depletion) HOST_DEPLETION="$next_val";  i=$((i+2)) ;;
            --min_coverage)   MIN_COVERAGE="$next_val";    i=$((i+2)) ;;
            --max_n_pct)      MAX_N_PCT="$next_val";       i=$((i+2)) ;;
            --min_segments)   MIN_SEGMENTS="$next_val";    i=$((i+2)) ;;
            --run_fastqc)     RUN_FASTQC="$next_val";      i=$((i+2)) ;;
            # v1.7.0
            --seq_type)         SEQ_TYPE="$next_val";          i=$((i+2)) ;;
            --ivar)             RUN_IVAR="$next_val";           i=$((i+2)) ;;
            --medaka)           RUN_MEDAKA="$next_val";         i=$((i+2)) ;;
            --ivar_freq)        IVAR_FREQ="$next_val";          i=$((i+2)) ;;
            --ivar_depth)       IVAR_DEPTH="$next_val";         i=$((i+2)) ;;
            --minority_freq)    MINORITY_FREQ_MIN="$next_val";  i=$((i+2)) ;;
            --coinfection_pct)  COINFECTION_PCT="$next_val";    i=$((i+2)) ;;
            --antiviral)        RUN_ANTIVIRAL="$next_val";      i=$((i+2)) ;;
            --h5_virulence)     RUN_H5_VIRULENCE="$next_val";   i=$((i+2)) ;;
            --fullvarcall)      RUN_FULLVARCALL="$next_val";     i=$((i+2)) ;;
            --gisaid_location)  GISAID_LOCATION="$next_val";    i=$((i+2)) ;;
            --gisaid_year)      GISAID_YEAR="$next_val";        i=$((i+2)) ;;
            --medaka_env)       CONDA_ENV_MEDAKA="$next_val";   i=$((i+2)) ;;
            --mode)
                forced_mode="$next_val"
                [ $i -le 4 ] && { input_dir="$irma_module"; output_dir="$forced_mode"; irma_module="${5:-}"; forced_mode="${6:-}"; }
                i=$((i+2))
                ;;
            *) i=$((i+1)) ;;
        esac
    done
}

