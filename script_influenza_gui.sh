detect_seq_mode() {
    local dir="$1"
    local forced="${2:-}"

    if [ -n "$forced" ]; then
        SEQ_MODE="$forced"
        log_info "Sequencing mode forced by user: $SEQ_MODE"
        return 0
    fi

    shopt -s nullglob

    # Check for long reads (ONT) first — mutually exclusive with short reads
    local ont=("$dir"/*.fastq.gz)
    local any_ont=0
    for f in "${ont[@]}"; do
        fname=$(basename "$f")
        # ONT files typically have no _R1/_R2/_1/_2 pattern
        if [[ "$fname" != *_R1* ]] && [[ "$fname" != *_R2* ]] &&            [[ "$fname" != *_1.fastq.gz ]] && [[ "$fname" != *_2.fastq.gz ]] &&            [[ "$fname" != *_L001_R1_001* ]] && [[ "$fname" != *_L001_R2_001* ]]; then
            any_ont=1
        fi
    done

    # Detect paired patterns (any mix of the three formats is accepted)
    local has_illumina=0 has_sra=0 has_generic=0

    local ill=("$dir"/*_L001_R1_001.fastq.gz)
    [ ${#ill[@]} -gt 0 ] && has_illumina=1

    local sra1=("$dir"/*_1.fastq.gz)
    for f in "${sra1[@]}"; do
        base="${f%_1.fastq.gz}"
        if [ -f "${base}_2.fastq.gz" ]; then
            has_sra=1; break
        fi
    done

    local gen=("$dir"/*_R1_*.fastq.gz "$dir"/*_R1.fastq.gz)
    # Exclude Illumina _L001_R1_001 from generic to avoid double-counting
    for f in "${gen[@]}"; do
        [[ "$f" == *_L001_R1_001.fastq.gz ]] && continue
        has_generic=1; break
    done

    if [ $((has_illumina + has_sra + has_generic)) -gt 0 ]; then
        SEQ_MODE="short_paired"
        shopt -u nullglob
        return 0
    fi

    # Check for single-end (no pair found for any _1.fastq.gz files)
    local sra1_unpaired=0
    for f in "${sra1[@]}"; do
        base="${f%_1.fastq.gz}"
        [ ! -f "${base}_2.fastq.gz" ] && sra1_unpaired=1 && break
    done

    local any=("$dir"/*.fastq.gz)
    if [ ${#any[@]} -gt 0 ]; then
        SEQ_MODE="single"
        shopt -u nullglob
        return 0
    fi

    shopt -u nullglob
    SEQ_MODE=""
}

list_samples() {
    local dir="$1"
    shopt -s nullglob
    local seen=()  # track processed sample names to avoid duplicates

    _already_seen() {
        local s="$1"
        for x in "${seen[@]}"; do [ "$x" = "$s" ] && return 0; done
        return 1
    }

    case "$SEQ_MODE" in

        short_paired)
            # Pattern 1 — Illumina: SAMPLE_L001_R1_001.fastq.gz / _L001_R2_001.fastq.gz
            for r1 in "$dir"/*_L001_R1_001.fastq.gz; do
                [ -f "$r1" ] || continue
                local name r2
                name=$(basename "$r1" _L001_R1_001.fastq.gz)
                r2="$dir/${name}_L001_R2_001.fastq.gz"
                _already_seen "$name" && continue
                seen+=("$name")
                echo -e "${name}\t${r1}\t${r2}"
            done

            # Pattern 2 — SRA/ENA: SAMPLE_1.fastq.gz / SAMPLE_2.fastq.gz
            for r1 in "$dir"/*_1.fastq.gz; do
                [ -f "$r1" ] || continue
                local name r2
                name=$(basename "$r1" _1.fastq.gz)
                r2="$dir/${name}_2.fastq.gz"
                # Only process if R2 exists (paired) and not already listed
                [ -f "$r2" ] || continue
                _already_seen "$name" && continue
                seen+=("$name")
                echo -e "${name}\t${r1}\t${r2}"
            done

            # Pattern 3 — Generic: SAMPLE_R1.fastq.gz / SAMPLE_R1_001.fastq.gz etc.
            for r1 in "$dir"/*_R1_*.fastq.gz "$dir"/*_R1.fastq.gz; do
                [ -f "$r1" ] || continue
                # Skip Illumina _L001_R1_001 already processed above
                [[ "$r1" == *_L001_R1_001.fastq.gz ]] && continue
                local name r2
                r2="${r1/_R1_/_R2_}"
                r2="${r2/_R1.fastq.gz/_R2.fastq.gz}"
                name=$(basename "$r1")
                name="${name/_R1*/}"
                name="${name%.fastq.gz}"
                _already_seen "$name" && continue
                seen+=("$name")
                echo -e "${name}\t${r1}\t${r2}"
            done
            ;;

        single|sra_single)
            for f in "$dir"/*.fastq.gz; do
                local name
                name=$(basename "$f" .fastq.gz)
                name="${name%_pass}"
                name="${name%_fail}"
                # Skip files that are clearly R2 or _2 to avoid listing pairs as singles
                [[ "$name" == *_R2* ]] && continue
                [[ "$name" == *_2 ]] && continue
                [[ "$name" == *_L001_R2_001 ]] && continue
                echo -e "${name}\t${f}"
            done
            ;;
    esac

    shopt -u nullglob
}

